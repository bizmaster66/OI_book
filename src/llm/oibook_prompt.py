def build_oibook_prompt(company: str, fulltext: str) -> str:
    return f"""
너는 대기업 오픈이노베이션 담당자와 투자자가 빠르게 판단할 수 있도록 IR Full text를 재구성하는 전문 애널리스트다.

[절대 규칙]
1) 아래 입력된 Full text에 '명시된 내용'만 사용한다. 추측/창작/일반론으로 사실을 채우지 않는다.
2) 숫자(시장규모/정확도/고객사/매출/PoC/파트너 등)는 Full text에 있는 경우에만 쓴다. 없으면 "IR에 구체 수치 미기재"라고 명시한다.
3) 출력은 반드시 'Markdown'이며, 아래 섹션 제목을 그대로 사용한다. 섹션 추가/삭제 금지.
4) 문체는 객관적이고 간결하되, 관심을 유도하도록 '가치/문제해결/도입효과' 중심으로 재서술한다.
5) 핵심포인트는 정확히 3개 bullet로 작성한다(기술력+문제해결 중심).

[출력 형식(Markdown) - 그대로 유지]
# {company} OI Book

## 핵심포인트
- ...
- ...
- ...

## 주요인원현황
- **이름/직함**: 핵심 경력 요약 → 이 사업에 적합한 이유(Full text 근거 기반)
(가능한 C-level 2~4명)

## 서비스/제품 소개
- **한 줄 정의**: 무엇을 해결하는가(가치 중심)
- **주요 서비스/제품/기술**
  - ...
- **도입 시 기대효과/적용 시나리오**
  - ...

## 비즈니스모델
- **타깃 고객**
- **수익화 방식**
- **과금/계약 단위(기재된 경우)**
- **판매/유통 채널**

## 차별점
- **차별 요소 1**
- **차별 요소 2**
- **검증/근거(트랙션/PoC/성능/인증 등, 기재된 경우)**

## 시장 분석 및 전략
- **시장 정의**
- **시장 규모/성장성(기재된 경우)**
- **진입/확장 전략**
- **개발/출시/확장 일정(기재된 경우)**

[입력]
회사명: {company}

Full text:
\"\"\"{fulltext}\"\"\"
""".strip()
